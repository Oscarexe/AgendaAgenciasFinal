    ID INTEGER NOT NULL GENERATED ALWAYS AS IDENTITY, -- Id autonum√©rico
    NOMBRE VARCHAR(20) NOT NULL,
    ANNOCREACION DATE,
    TELEFONO VARCHAR(15),
    EMAIL VARCHAR(30),
    PROVINCIA VARCHAR(20),
    PRECIOHORA DECIMAL (7,2),
    SOPORTE VARCHAR (15),
    COBRADESPSERVICIO BOOLEAN,
    ISBN VARCHAR(12),
