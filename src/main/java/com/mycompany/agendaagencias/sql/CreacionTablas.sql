DROP TABLE AGENCIASPUBLICITARIAS;
DROP TABLE ambito;
CREATE TABLE AMBITO (
    ID INTEGER NOT NULL GENERATED ALWAYS AS IDENTITY,
    CODIGO CHAR(2),
    AMBITO VARCHAR(20) NOT NULL,
    CONSTRAINT ID_AMBITO_PK PRIMARY KEY (ID)
);


